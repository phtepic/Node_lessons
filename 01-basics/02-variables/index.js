////////////////////VARIABLES///////////////////
var var_numer = 1;
var var_text = "Hello";
var var_bool = true;

let let_numer = 1;
let let_text = "Hello";
let let_bool = true;

const const_numer = 1;
const const_text = "Hello";
const const_bool = true;

function print_variables() {
  console.log("var " + var_numer);
  console.log("var " + var_text);
  console.log("var " + var_bool);
  console.log("let " + let_numer);
  console.log("let " + let_text);
  console.log("let " + let_text);
  console.log("const " + const_numer);
  console.log("const " + const_text);
  console.log("const " + const_text);
}

print_variables();
